// Simple categorization helpers
export function getCategoryForModel(model) {
    const id = model.id.toLowerCase();
    const name = model.name?.toLowerCase() ?? '';
    const description = model.description?.toLowerCase() ?? '';

    if (id.includes('concept') || name.includes('concept') || description.includes('concept')) {
        return 'Concept Car';
    }

    if (id.startsWith('dbx')) {
        return 'SUV';
    }

    if (id.includes('valkyrie') || id.includes('valhalla') || id.includes('vulcan') || id.includes('one-77')) {
        return 'Hypercar';
    }

    if (id.includes('victor') || id.includes('valour') || id.includes('valiant') || id.includes('v12-speedster')) {
        return 'Limited Series';
    }

    if (id.includes('cygnet')) {
        return 'City Car';
    }

    if (id.includes('rapide') || id.includes('lagonda')) {
        return 'Sedan / 4-Door GT';
    }

    if (id.includes('db5') || id.includes('db4') || id.includes('db6') || id.includes('db2') || id.includes('db1')) {
        return 'Classic DB Series';
    }

    if (id.includes('db7') || id.includes('db9') || id.includes('db11') || id.includes('db12')) {
        return 'Modern DB Series';
    }

    if (id.includes('vanquish') || id.includes('virage') || id.includes('dbs')) {
        return 'Super GT';
    }

    if (id.includes('vantage')) {
        return 'Sports Car';
    }

    if (id.includes('v8') || id.includes('v12')) {
        return 'V-Series GT';
    }

    return 'Other';
}

export function groupModelsByCategory() {
    const groups = {};

    models.forEach((model) => {
        const category = getCategoryForModel(model);
        if (!groups[category]) {
            groups[category] = [];
        }
        groups[category].push(model);
    });

    return Object.entries(groups).map(([category, items]) => ({
        category,
        models: items,
    }));
}

